import type { ResolvedConfig } from '../../../types.js';
import { getAllRules } from '../../../engine/registry.js';

/**
 * Build the .claude/rules/vguard-enforcement.md content from resolved config.
 *
 * This file instructs the AI agent about which VGuard rules are active and
 * what to expect from the hook enforcement system.
 */
export function buildEnforcementRulesMarkdown(config: ResolvedConfig): string {
  const allRules = getAllRules();

  // Group active rules by category
  const categories = new Map<
    string,
    Array<{ id: string; name: string; description: string; severity: string }>
  >();

  let totalCount = 0;

  for (const [ruleId, ruleConfig] of config.rules) {
    if (!ruleConfig.enabled) continue;

    const rule = allRules.get(ruleId);
    if (!rule) continue;

    totalCount++;
    const category = ruleId.split('/')[0];
    const existing = categories.get(category) ?? [];
    existing.push({
      id: ruleId,
      name: rule.name,
      description: rule.description,
      severity: ruleConfig.severity,
    });
    categories.set(category, existing);
  }

  // Build sections
  const sections: string[] = [
    '<!-- Generated by VGuard — do not edit manually. -->',
    '<!-- Regenerate with: npx vguard generate -->',
    '',
    '# VGuard Enforcement Rules',
    '',
    `This project uses [VGuard](https://github.com/anthril/vibe-guard) for AI coding guardrails.`,
    `**${totalCount} rules** are actively enforced via runtime hooks.`,
    '',
  ];

  // Emit each category
  for (const [category, rules] of categories) {
    const label = category.charAt(0).toUpperCase() + category.slice(1);
    sections.push(`## ${label} (${rules.length})`);
    sections.push('');

    for (const rule of rules) {
      sections.push(`- **${rule.name}** (\`${rule.id}\`) [${rule.severity}]: ${rule.description}`);
    }
    sections.push('');
  }

  // Instructions for the AI agent
  sections.push('## How Enforcement Works');
  sections.push('');
  sections.push('1. **PreToolUse hooks** check edits and commands before they execute');
  sections.push('2. **PostToolUse hooks** verify results after tool use completes');
  sections.push('3. **Stop hooks** run final checks at session end');
  sections.push('');
  sections.push('If a hook **blocks** your operation, it will provide a message explaining why');
  sections.push('and suggest a fix. Follow the suggestion to resolve the issue.');
  sections.push('');
  sections.push('## Quick Reference');
  sections.push('');
  sections.push('| Command | Purpose |');
  sections.push('|---------|---------|');
  sections.push('| `npx vguard lint` | Scan for violations |');
  sections.push('| `npx vguard doctor` | Check VGuard health |');
  sections.push('| `npx vguard fix` | Auto-fix violations |');
  sections.push('| `npx vguard report` | Generate quality dashboard |');
  sections.push('');
  sections.push('Configuration: `vguard.config.ts`');
  sections.push('');

  return sections.join('\n');
}
